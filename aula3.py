"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas (simples ou duplas)
"""
# Sem aspas (no caso, um numeral)
print(1234)

# Aspas Simples
print('Daniel Carvalho')
print(1, 'Daniel "Carvalho"')

# Aspas Duplas
print("Daniel Carvalho")
print(2, "Daniel 'Carvalho'")

# Escape
print("Daniel \"Carvalho\"")

# Exibir o caracter de escape
print(r"Daniel \"Carvalho\"")